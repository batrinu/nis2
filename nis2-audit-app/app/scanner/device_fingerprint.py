class DeviceFingerprinter:
    """Identify device vendors and types from network information."""
    
    @staticmethod
    def identify_vendor(mac_address: Optional[str]) -> Optional[str]:
        """
        Identify vendor from MAC address OUI.
        
        Args:
            mac_address: MAC address string (e.g., "00:1a:2b:3c:4d:5e")
        
        Returns:
            Vendor name or None
        """
        if not mac_address:
            return None
        
        # Normalize MAC address
        mac = mac_address.lower().replace("-", ":").replace(".", ":")
        
        # Extract OUI (first 3 octets)
        oui_match = re.match(r"^([0-9a-f]{2}:[0-9a-f]{2}:[0-9a-f]{2})", mac)
        if not oui_match:
            return None
        
        oui = oui_match.group(1)
        return VENDOR_OUIS.get(oui)
    
    @staticmethod
    def identify_device_type(
        host: DiscoveredHost,
        vendor: Optional[str] = None
    ) -> str:
        """
        Identify device type from host information.
        
        Args:
            host: DiscoveredHost with port and OS info
            vendor: Optional vendor name
        
        Returns:
            Device type string
        """
        ports = set(host.open_ports)
        vendor = vendor or host.vendor or ""
        vendor_lower = vendor.lower()
        
        # Router indicators
        if any(p in ports for p in [8291]):  # Winbox (MikroTik)
            return "router"
        
        if ("mikrotik" in vendor_lower or "router" in host.os_guess.lower()) if host.os_guess else False:
            return "router"
        
        if "cisco" in vendor_lower and (22 in ports or 23 in ports):
            # Could be router or switch - need more info
            return "network_device"
        
        # Firewall indicators
        if any(p in ports for p in [443, 8443, 10443]):
            if "fortinet" in vendor_lower or "palo" in vendor_lower or "checkpoint" in vendor_lower:
                return "firewall"
            if "firewall" in host.os_guess.lower() if host.os_guess else False:
                return "firewall"
        
        if ("fortinet" in vendor_lower or "fortigate" in host.os_guess.lower()) if host.os_guess else False:
            return "firewall"
        
        # Switch indicators
        if ("switch" in host.os_guess.lower()) if host.os_guess else False:
            return "switch"
        
        if "aruba" in vendor_lower or "hp" in vendor_lower:
            return "switch"
        
        # Server indicators
        if 22 in ports or 3389 in ports or 5985 in ports:
            if host.os_family in ["Linux", "Windows", "FreeBSD"]:
                return "server"
        
        # Workstation indicators
        if host.os_family == "Windows" and 3389 in ports:
            return "workstation"
        
        # Default to unknown
        return "unknown"
    
    @classmethod
    def fingerprint(cls, host: DiscoveredHost) -> NetworkDevice:
        """
        Create a NetworkDevice with fingerprinted information.
        
        Args:
            host: DiscoveredHost from network scan
        
        Returns:
            NetworkDevice with vendor/type identified
        """
        # Identify vendor from MAC or existing info
        vendor = host.vendor or cls.identify_vendor(host.mac_address)
        
        # Identify device type
        device_type = cls.identify_device_type(host, vendor)
        
        return NetworkDevice(
            session_id=host.ip_address,  # Will be set properly by caller
            ip_address=host.ip_address,
            hostname=host.hostname,
            mac_address=host.mac_address,
            vendor=vendor,
            device_type=device_type,
            os_version=host.os_guess,
            model=None,  # Will be filled after SSH interrogation
            open_ports=host.open_ports,
            discovery_method="nmap_scan",
        )
